    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:

        int index = tblEmpleados.getSelectedRow();

        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Selecciona una fila", "Acceso no permitido", 2);
        } else {
            try {
                registro = (Vector) dtm.getDataVector().elementAt(index);
                System.out.println(registro.elementAt(0).toString());
                JPanel pnlEditar = new PnlEditarEmpleado();
                String comando = "SELECT * FROM empleado WHERE idempleado = '" + registro.elementAt(0).toString() + "'";
                PreparedStatement pst = pnlInicioSesion.conn.getConnSin().prepareStatement(comando);
                ResultSet rs = pst.executeQuery();
                PnlEditarEmpleado.txtIDEmp.setEditable(false);
                while (rs.next()) {
                    PnlEditarEmpleado.txtIDEmp.setText(rs.getString(1));
                    PnlEditarEmpleado.txtNombre.setText(rs.getString(2));
                    PnlEditarEmpleado.txtCedula.setText(rs.getString(3));
                    PnlEditarEmpleado.txtTelefono.setText(rs.getString(4));
                    PnlEditarEmpleado.txtDireccion.setText(rs.getString(5));
                }
                agregarPanel(pnlEditar);
                pst.execute();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }


    }//GEN-LAST:event_btnEditarActionPerformed

