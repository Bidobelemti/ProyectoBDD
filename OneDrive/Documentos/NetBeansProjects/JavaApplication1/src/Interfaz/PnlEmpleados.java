    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        limpiarTabla();
        String comando = "";
        String numerosucur = cmbSucursales.getItemAt(cmbSucursales.getSelectedIndex());
        String cedula = txtCedulaEmp.getText().trim();
        String nombre = txtNombreEmp.getText().trim();
        if (txtCedulaEmp.getText().trim().isEmpty() && txtNombreEmp.getText().trim().isEmpty()) {
            if (numerosucur.isBlank()) {
                comando = "SELECT * FROM empleado";
            } else {
                comando = "SELECT * FROM empleado "
                        + "JOIN trabaja ON empleado.idempleado = trabaja.idempleado "
                        + "WHERE trabaja.numerosucur = " + numerosucur;
            }

        } else {
            // Construimos la consulta basada en las condiciones
            StringBuilder sb = new StringBuilder("SELECT * FROM empleado ");
            boolean hasJoin = false;

            if (!numerosucur.isBlank()) {
                sb.append("JOIN trabaja ON empleado.idempleado = trabaja.idempleado ");
                hasJoin = true;
            }

            sb.append("WHERE ");

            if (!cedula.isEmpty()) {
                sb.append("empleado.idempleado = '").append(cedula).append("' ");
                if (!nombre.isEmpty() || !numerosucur.isBlank()) {
                    sb.append("AND ");
                }
            }

            if (!nombre.isEmpty()) {
                sb.append("empleado.nombre LIKE '%").append(nombre).append("%' ");
                if (!numerosucur.isBlank()) {
                    sb.append("AND ");
                }
            }

            if (!numerosucur.isBlank()) {
                sb.append("trabaja.numerosucur = '").append(numerosucur).append("' ");
            }

            comando = sb.toString();
        }
        System.out.println(comando);
        LlenarTabla(comando);
    }//GEN-LAST:event_btnBuscarActionPerformed

